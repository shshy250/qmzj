function faceClick(val) {
			switch (val) {
				case 'good':
					$('.good-T').show();
					$('.mid-T').hide();
					$('.low-T').hide();
					$('.good-F').hide();
					$('.mid-F').show();
					$('.low-F').show();
					break;
				case 'mid':
					$('.good-T').hide();
					$('.mid-T').show();
					$('.low-T').hide();
					$('.good-F').show();
					$('.mid-F').hide();
					$('.low-F').show();
					break;
				case 'low':
					$('.good-T').hide();
					$('.mid-T').hide();
					$('.low-T').show();
					$('.good-F').show();
					$('.mid-F').show();
					$('.low-F').hide();
					break;
				default:
					break;
			}
		}

